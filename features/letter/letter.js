const SIGNATURE = 'מהאחד והיחיד יותם💐😘😎';

export function initLetter() {
  const body = document.querySelector('.letter__body');
  const sparklesContainer = document.querySelector('.letter__sparkles');

  const paraEls = [];
  LETTER_PARAGRAPHS.forEach(text => {
    const p = document.createElement('p');
    p.classList.add('letter__paragraph');
    p.textContent = text;
    body.appendChild(p);
    paraEls.push(p);
  });

  const sig = document.createElement('p');
  sig.classList.add('letter__signature');
  sig.textContent = SIGNATURE;
  body.appendChild(sig);

  // Sparkles
  for (let i = 0; i < 25; i++) {
    const s = document.createElement('div');
    s.classList.add('letter__sparkle');
    s.style.left = `${Math.random() * 100}%`;
    s.style.top = `${Math.random() * 100}%`;
    s.style.setProperty('--dur', `${2 + Math.random() * 3}s`);
    s.style.setProperty('--delay', `${Math.random() * 4}s`);
    s.style.width = s.style.height = `${3 + Math.random() * 4}px`;
    sparklesContainer.appendChild(s);
  }

  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
      }
    });
  }, { threshold: 0.3 });

  paraEls.forEach(p => observer.observe(p));
  observer.observe(sig);
}
